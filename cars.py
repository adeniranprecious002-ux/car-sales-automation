  pc = Pie()
  pc.x = 20
  pc.y = 20
  pc.width = 160
  pc.height = 160
  pc.data = data
  pc.labels = [str(sales) for sales in data]
  pc.sideLabels = True
  d.add(pc)

  # Configure Chart Legend layout
  legend = Legend()
  legend.x = 240
  legend.y = 180
  legend.dx = 8
  legend.dy = 8
  legend.fontName = 'Helvita'
  legend.fontSize = 9
  legend.boxAnchor = 'nw'
  legend.columnMaximum = 'right'
  legend.colorNamePairs = [(pc.slices[i].fillColor, labels[i]) for i in range(len(data))]
  d.add(legend)

  return d

