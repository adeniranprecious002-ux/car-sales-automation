  # Add sales data together by make
  make_sales = collections.defaultdict(int)
  for item in car_data:
    make_sales[item["car"]["car_make"]] += item["total_sales"]

  # Sort data Layout for clean colour slice matching
  sorted_makes = sorted(make_sales.items(), key=lambda x: x[1], reverse=True)
  labels = [make for make, sales in sorted_makes]
  data = [sales for make, sales in sorted_makes]

  # Initialize drawing container
  d =  Drawing(400, 200)

